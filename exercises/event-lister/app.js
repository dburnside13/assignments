function buttonRed() { document.getElementById("color").style.backgroundColor = "red" }

document.getElementById("color").addEventListener("mousedown", buttonRed)

function buttonGreen() { document.getElementById("color").style.backgroundColor = "green" }

document.getElementById("color").addEventListener("dblclick", buttonGreen)

function buttonBlue() { document.getElementById("color").style.backgroundColor = "blue" }

document.getElementById("color").addEventListener("mouseover", buttonBlue)

function buttonOrange() { document.getElementById("color").style.backgroundColor = "orange" }

document.body.addEventListener("wheel", buttonOrange)

function buttonYellow() { document.getElementById("color").style.backgroundColor = "yellow" }

document.getElementById("color").addEventListener("mouseup", buttonYellow)











